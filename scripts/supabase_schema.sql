create table if not exists public.users (
  id bigint generated by default as identity primary key,
  login text not null,
  password_hash text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists idx_users_login_unique
  on public.users(lower(login));

alter table public.users enable row level security;

create table if not exists public.remuneration_config (
  id bigint generated by default as identity primary key,
  valor_base numeric not null default 0,
  valor_hora numeric not null default 0,
  valor_bonus numeric not null default 0,
  valor_aux_transporte numeric not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists idx_remuneration_config_singleton
  on public.remuneration_config ((id));

alter table public.remuneration_config enable row level security;
